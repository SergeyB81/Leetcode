def move_zeros1(arr):
    """
    Функция перемещает все нули в конец массива, сохраняя порядок ненулевых элементов.
    Работает in-place (модифицирует исходный список).

    Алгоритм: метод двух указателей (two pointers)
    - zero_ind указывает на позицию, куда нужно поместить следующий ненулевой элемент
    - Проходим по массиву и меняем местами ненулевые элементы с нулями
    """

    # zero_ind - указатель на позицию первого нуля в обработанной части массива
    # Изначально None, так как мы еще не нашли первый ноль
    zero_ind = None

    # Проходим по всем элементам массива, i - текущий индекс
    for i in range(len(arr)):

        # --- СЛУЧАЙ 1: Нашли ноль И это первый ноль в массиве ---
        # Проверяем: текущий элемент равен 0 И указатель zero_ind еще не установлен
        if (arr[i] == 0) and (zero_ind is None):
            # Запоминаем позицию первого найденного нуля
            zero_ind = i
            # continue пропускает оставшийся код цикла и переходит к следующей итерации
            # Это важно, чтобы не пытаться менять местами ноль с самим собой
            continue

        # --- СЛУЧАЙ 2: Уже есть найденный ноль И текущий элемент не ноль ---
        # Проверяем: zero_ind уже указывает на позицию нуля И текущий элемент != 0
        if (zero_ind is not None) and (arr[i] != 0):
            # Меняем местами ноль (на позиции zero_ind) с ненулевым элементом (на позиции i)
            # После обмена ноль перемещается на позицию i, а ненулевой элемент - на позицию zero_ind
            arr[zero_ind], arr[i] = arr[i], arr[zero_ind]

            # Увеличиваем zero_ind на 1, чтобы он указывал на следующий ноль
            # (который теперь находится на позиции zero_ind + 1, так как после обмена
            # все нули идут подряд, начиная с исходной позиции первого нуля)
            zero_ind += 1

    # Возвращаем модифицированный массив (хотя изменения уже произошли in-place)
    return arr



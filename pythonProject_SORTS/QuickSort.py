class QuickSort:
    """
    БЫСТРАЯ СОРТИРОВКА (Quick Sort)

    Смысл: Алгоритм использует стратегию "разделяй и властвуй". Выбирается опорный элемент
    (pivot), массив разделяется на элементы меньше опорного и больше опорного, затем
    рекурсивно сортируются полученные подмассивы.

    Преимущества:
    - Очень быстрая на практике (название говорит само за себя)
    - Сортирует на месте (не требует дополнительной памяти, кроме стека вызовов)
    - Хорошая локализация обращений к памяти (кэш-дружественность)

    Недостатки:
    - Нестабильная сортировка (порядок равных элементов может измениться)
    - В худшем случае O(n²) - если неудачно выбирать опорный элемент

    Сложность: O(n log n) в среднем, O(n²) в худшем
    Память: O(log n) для стека рекурсии
    """

    def sort(self, arr, low=0, high=None):
        """
        Основной метод сортировки

        Args:
            arr: список для сортировки
            low: начальный индекс
            high: конечный индекс

        Returns:
            отсортированный список (тот же объект)
        """
        if high is None:
            high = len(arr) - 1

        if low < high:
            # Разделяем массив и получаем индекс опорного элемента
            pivot_index = self._partition(arr, low, high)

            # Рекурсивно сортируем левую и правую части
            self.sort(arr, low, pivot_index - 1)
            self.sort(arr, pivot_index + 1, high)

        return arr

    def _partition(self, arr, low, high):
        """
        Разделение массива относительно опорного элемента

        Выбираем последний элемент как опорный, все элементы меньше него
        перемещаем влево, больше - вправо
        """
        pivot = arr[high]  # опорный элемент (можно выбрать иначе)
        i = low - 1  # индекс меньшего элемента

        for j in range(low, high):
            # Если текущий элемент меньше или равен опорному
            if arr[j] <= pivot:
                i += 1
                arr[i], arr[j] = arr[j], arr[i]  # меняем местами

        # Ставим опорный элемент на правильную позицию
        arr[i + 1], arr[high] = arr[high], arr[i + 1]
        return i + 1


# Пример использования
if __name__ == "__main__":
    # Тестовые данные
    test_array = [64, 34, 25, 12, 22, 11, 90, 5, 77, 30]
    print(f"Исходный массив: {test_array}")

    # Создаем экземпляр сортировщика
    sorter = QuickSort()

    # Сортируем (копию массива, чтобы сохранить оригинал)
    sorted_array = sorter.sort(test_array.copy())
    print(f"Отсортированный массив: {sorted_array}")

    # Проверка на частично отсортированных данных
    nearly_sorted = [1, 2, 3, 5, 4, 6, 7, 8, 9, 10]
    print(f"\nЧастично отсортированный: {nearly_sorted}")
    print(f"Результат: {sorter.sort(nearly_sorted.copy())}")